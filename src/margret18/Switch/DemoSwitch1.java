package margret18.Switch;

public class DemoSwitch1 {

	public static void main(String[] args) {
		int q = 4;
		switch(q)
		{
		case 1:
			System.out.println(1);
			break;
		case 2:
			System.out.println(2);
			break;
		case 3:
			System.out.println(4);
			break;
		case 4:
			System.out.println(5);
			break;
		}
	}

}

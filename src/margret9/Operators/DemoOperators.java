package margret9.Operators;

public class DemoOperators {

	public static void main(String[] args) {
		int n1 = 5;
		int n2 = 10;
		int sum = n1 + n2;
		int product = n1 * n2;
		int multiplication = n1 - n2;
		int division = n2 / n1;
		int remainder = n2 % n1;
		System.out.printf("Sum: %d%n Product: %d%n Multiplication: %d%n Division: %d%n Remainder: %d%n ",
				sum,product,multiplication,division,remainder);

	}

}

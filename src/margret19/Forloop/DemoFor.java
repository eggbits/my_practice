package margret19.Forloop;

public class DemoFor {

	public static void main(String[] args) {
		for(int i = 0; i < 4; i++)
		{
			System.out.print("1 2 3 4 5   ");
			System.out.println(i);
		}
		System.out.println();
		for(int i =0; i < 5 ; i++)
		{
			System.out.println("1 2 3 4 5 6");
		}
		System.out.println();
		for(int i = 0; i < 2;i++)
		{
			System.out.println("*");
			System.out.println("**");
			System.out.println("***");
		}
		System.out.println();
		for(int i = 0; i < 3; i++)
		{
			System.out.println("I Like Java");
		}
	}

}

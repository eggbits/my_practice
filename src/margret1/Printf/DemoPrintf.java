package margret1.Printf;

public class DemoPrintf {

	public static void main(String[] args) {
		//Format specifiers
		System.out.printf("it is %d O'clock %n",12);
		System.out.printf("my name %s and curently working as %s","Hareesh","Software Developer");
		System.out.println();
		System.out.printf("my son name is %s and my daugher name is %s","Anagh","Anagha");
		
		//Common Escape Sequence
		System.out.println("\n");
		System.out.println("one\ntwo\nthree\n");
		System.out.println("one\ttwo\tthree\t");
		System.out.println("A backslash \\ and double\"");
		System.out.println("red\nblue\tgreen\\yellow\"");
	}
}
